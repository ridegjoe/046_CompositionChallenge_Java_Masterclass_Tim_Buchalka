package eu.rideg;

public class Resolution {
    // Definition of fields
    private int width;
    private int height;

    // Definition of constructor for initializing the values
    public Resolution(int width, int height) {
        this.width = width;
        this.height = height;
    }


    // Getters
    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

}
